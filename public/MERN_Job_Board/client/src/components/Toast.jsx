import { useAuth } from "../context/AuthContext";

export default function Toast() {
  const { toasts } = useAuth();
  if (!toasts.length) return null;
  return (
    <div className="toast-container">
      {toasts.map((t) => (
        <div key={t.id} className={`toast ${t.type}`}>
          {t.message}
        </div>
      ))}
    </div>
  );
}
