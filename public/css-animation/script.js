/**
 * LayerText — CSS Text Animation Generator
 * script.js
 *
 * Handles all user input, live preview rendering,
 * code generation, and clipboard export.
 */

'use strict';

/* ─── DOM References ─────────────────────────── */
const els = {
  txt:        document.getElementById('txt-input'),
  bgColor:    document.getElementById('bg-color'),
  bgHex:      document.getElementById('bg-hex'),
  txtColor:   document.getElementById('txt-color'),
  txtHex:     document.getElementById('txt-hex'),
  layers:     document.getElementById('layers'),
  layersVal:  document.getElementById('layers-val'),
  animDur:    document.getElementById('anim-dur'),
  animDurVal: document.getElementById('anim-dur-val'),
  delayOff:   document.getElementById('delay-offset'),
  delayVal:   document.getElementById('delay-val'),
  fontSize:   document.getElementById('font-size'),
  fontVal:    document.getElementById('font-val'),
  stage:      document.getElementById('preview-stage'),
  htmlOut:    document.getElementById('html-out'),
  cssOut:     document.getElementById('css-out'),
};

/* ─── State ─────────────────────────────────── */
let styleTag = null; // <style> injected into the page for preview

/* ─── Code Generators ───────────────────────── */

/**
 * Build the HTML block that gets exported and also injected into the preview.
 * @param {string} text  - The phrase to display
 * @param {number} n     - Number of layers
 * @returns {string}
 */
function generateHTML(text, n) {
  const safe = escapeHTML(text || 'CSS Text Animation');
  const rows = Array.from({ length: n }, (_, i) =>
    `  <p style="--i: ${i + 1}">${safe}</p>`
  ).join('\n');
  return `<div class="box">\n${rows}\n</div>`;
}

/**
 * Build the standalone CSS string (for export).
 * @param {string} bg       - Background hex color
 * @param {string} color    - Text / stroke hex color
 * @param {number} dur      - Animation duration in seconds
 * @param {number} offset   - Per-layer delay offset in seconds
 * @param {number} fs       - Font size in rem
 * @returns {string}
 */
function generateCSS(bg, color, dur, offset, fs) {
  return `/* Generated by LayerText — https://layertext.app */

body {
  margin: 0;
  min-height: 100vh;
  display: flex;
  align-items: center;
  justify-content: center;
  background: ${bg};
}

.box {
  display: flex;
  flex-direction: column;
  align-items: center;
}

.box p {
  font-size: ${fs}rem;
  font-family: Arial, Helvetica, sans-serif;
  font-weight: 700;
  -webkit-text-stroke: 1px ${color};
  color: transparent;
  text-transform: uppercase;
  letter-spacing: 0.05em;
  line-height: 1.15;
  animation: layerAnimate ${dur}s linear infinite;
  animation-delay: calc(-${offset}s * var(--i));
}

@keyframes layerAnimate {
  0%, 10% {
    color: ${color};
    filter: hue-rotate(0deg);
  }
  10.1% {
    color: transparent;
  }
  100% {
    color: transparent;
    filter: hue-rotate(720deg);
  }
}`;
}

/**
 * Build the preview-scoped CSS injected into the page.
 * Uses a .preview-stage .box scope so it doesn't affect the generator UI.
 */
function generatePreviewCSS(bg, color, dur, offset, fs) {
  return `
    #preview-stage {
      background: ${bg};
    }
    #preview-stage .box p {
      font-size: clamp(0.9rem, ${fs * 0.7}vw + 0.4rem, ${fs}rem);
      font-family: Arial, Helvetica, sans-serif;
      font-weight: 700;
      -webkit-text-stroke: 1px ${color};
      color: transparent;
      text-transform: uppercase;
      letter-spacing: 0.05em;
      line-height: 1.15;
      animation: layerAnimate ${dur}s linear infinite;
      animation-delay: calc(-${offset}s * var(--i));
    }
    @keyframes layerAnimate {
      0%, 10% {
        color: ${color};
        filter: hue-rotate(0deg);
      }
      10.1% {
        color: transparent;
      }
      100% {
        color: transparent;
        filter: hue-rotate(720deg);
      }
    }
  `;
}

/* ─── Preview Renderer ──────────────────────── */

/**
 * Inject or update a <style> tag for the preview, and re-render the .box HTML.
 */
function renderPreview(config) {
  const { text, bg, color, layers, dur, offset, fs } = config;

  // Style injection
  if (!styleTag) {
    styleTag = document.createElement('style');
    styleTag.id = 'preview-styles';
    document.head.appendChild(styleTag);
  }
  styleTag.textContent = generatePreviewCSS(bg, color, dur, offset, fs);

  // HTML injection into the stage
  const safe = escapeHTML(text || 'CSS Text Animation');
  const rows = Array.from({ length: layers }, (_, i) =>
    `<p style="--i: ${i + 1}">${safe}</p>`
  ).join('');
  els.stage.innerHTML = `<div class="box">${rows}</div>`;
}

/* ─── Output Updater ────────────────────────── */

function update() {
  const config = readConfig();
  const { text, bg, color, layers, dur, offset, fs } = config;

  // Update display readouts
  els.bgHex.textContent       = bg;
  els.txtHex.textContent      = color;
  els.layersVal.textContent   = layers;
  els.animDurVal.textContent  = `${dur.toFixed(1)}s`;
  els.delayVal.textContent    = `−${offset.toFixed(2)}s`;
  els.fontVal.textContent     = `${fs.toFixed(2)}rem`;

  // Render live preview
  renderPreview(config);

  // Update export code blocks
  els.htmlOut.value = generateHTML(text, layers);
  els.cssOut.value  = generateCSS(bg, color, dur, offset, fs);
}

/* ─── Read Config ───────────────────────────── */

function readConfig() {
  return {
    text:   els.txt.value.trim() || 'CSS Text Animation',
    bg:     els.bgColor.value,
    color:  els.txtColor.value,
    layers: parseInt(els.layers.value, 10),
    dur:    parseFloat(els.animDur.value),
    offset: parseFloat(els.delayOff.value),
    fs:     parseFloat(els.fontSize.value),
  };
}

/* ─── Copy Buttons ──────────────────────────── */

document.querySelectorAll('.copy-btn').forEach(btn => {
  btn.addEventListener('click', () => {
    const targetId = btn.dataset.target;
    const textarea = document.getElementById(targetId);
    if (!textarea) return;

    navigator.clipboard.writeText(textarea.value).then(() => {
      const label = btn.querySelector('span');
      const originalText = label.textContent;
      btn.classList.add('copied');
      label.textContent = 'Copied!';
      setTimeout(() => {
        btn.classList.remove('copied');
        label.textContent = originalText;
      }, 2000);
    }).catch(() => {
      // Fallback for older browsers
      textarea.select();
      document.execCommand('copy');
    });
  });
});

/* ─── Event Listeners ───────────────────────── */

const inputs = [
  els.txt, els.bgColor, els.txtColor,
  els.layers, els.animDur, els.delayOff, els.fontSize,
];

inputs.forEach(el => el.addEventListener('input', update));

/* ─── Utils ─────────────────────────────────── */

/**
 * Escape special HTML characters to prevent XSS in the preview.
 * @param {string} str
 * @returns {string}
 */
function escapeHTML(str) {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

/* ─── Init ──────────────────────────────────── */

update();