const gridContainer = document.getElementById('grid');

// Create 9x9 inputs
for (let i = 0; i < 81; i++) {

    const input = document.createElement('input');
    input.type = 'text';
    input.maxLength = 1;
    input.inputMode = 'numeric';

    input.oninput = (e) => {
        if (!/^[1-9]$/.test(e.target.value)) {
            e.target.value = '';
            input.classList.remove('user-input');
        } else {
            input.classList.add('user-input');
        }
    };

    gridContainer.appendChild(input);
}

// Get board from UI
function getGrid() {

    const inputs = gridContainer.getElementsByTagName('input');
    let board = [];

    for (let row = 0; row < 9; row++) {
        let currentRow = [];
        for (let col = 0; col < 9; col++) {
            const value = inputs[row * 9 + col].value;
            currentRow.push(
                value === '' ? 0 : parseInt(value)
            );
        }
        board.push(currentRow);
    }

    return board;
}

// Update UI and apply appropriate developer theme coloring
function setGrid(board) {

    const inputs = gridContainer.getElementsByTagName('input');

    for (let row = 0; row < 9; row++) {
        for (let col = 0; col < 9; col++) {
            const index = row * 9 + col;

            if (board[row][col] === 0) {
                inputs[index].value = '';
                inputs[index].classList.remove('solved-cell', 'user-input');
            } else {
                // If cell is empty and doesn't have user input, it's generated by the solver
                if (inputs[index].value === '' && !inputs[index].classList.contains('user-input')) {
                    inputs[index].classList.add('solved-cell');
                }
                inputs[index].value = board[row][col];
            }
        }
    }
}

// Validate placement
function isValid(board, row, col, num) {

    for (let i = 0; i < 9; i++) {
        if (board[row][i] === num || board[i][col] === num) {
            return false;
        }
    }

    const startRow = Math.floor(row / 3) * 3;
    const startCol = Math.floor(col / 3) * 3;

    for (let i = 0; i < 3; i++) {
        for (let j = 0; j < 3; j++) {
            if (board[startRow + i][startCol + j] === num) {
                return false;
            }
        }
    }

    return true;
}

// Sleep helper
function sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

// Highlight active cell
function highlightCell(row, col) {

    clearHighlights();
    const inputs = gridContainer.getElementsByTagName('input');
    const index = row * 9 + col;
    inputs[index].classList.add('active-cell');
}

// Clear highlights
function clearHighlights() {

    const inputs = gridContainer.getElementsByTagName('input');
    for (let input of inputs) {
        input.classList.remove('active-cell');
    }
}

// Animated solver
async function solver(board) {

    for (let row = 0; row < 9; row++) {
        for (let col = 0; col < 9; col++) {

            if (board[row][col] === 0) {
                for (let num = 1; num <= 9; num++) {

                    if (isValid(board, row, col, num)) {
                        board[row][col] = num;
                        setGrid(board);
                        highlightCell(row, col);

                        await sleep(20); // Faster processing speed for high-tech vibe

                        if (await solver(board)) {
                            return true;
                        }

                        board[row][col] = 0;
                        setGrid(board);
                        highlightCell(row, col);

                        await sleep(20);
                    }
                }
                return false;
            }
        }
    }
    return true;
}

// Solve Sudoku
async function solveSudoku() {

    const board = getGrid();
    const buttons = document.querySelectorAll('button');

    buttons.forEach(btn => { btn.disabled = true; });

    const solved = await solver(board);

    if (!solved) {
        alert('ERR: Verification Failed. No solution exists.');
    }

    clearHighlights();
    buttons.forEach(btn => { btn.disabled = false; });
}

// Clear board
function clearGrid() {

    const inputs = gridContainer.getElementsByTagName('input');
    for (let input of inputs) {
        input.value = '';
        input.classList.remove('user-input', 'solved-cell', 'active-cell');
    }
}

// Random puzzle generator
function generatePuzzle() {

    clearGrid();

    const puzzles = [
        [
            [5,3,0,0,7,0,0,0,0],
            [6,0,0,1,9,5,0,0,0],
            [0,9,8,0,0,0,0,6,0],
            [8,0,0,0,6,0,0,0,3],
            [4,0,0,8,0,3,0,0,1],
            [7,0,0,0,2,0,0,0,6],
            [0,6,0,0,0,0,2,8,0],
            [0,0,0,4,1,9,0,0,5],
            [0,0,0,0,8,0,0,7,9]
        ],
        [
            [0,0,0,2,6,0,7,0,1],
            [6,8,0,0,7,0,0,9,0],
            [1,9,0,0,0,4,5,0,0],
            [8,2,0,1,0,0,0,4,0],
            [0,0,4,6,0,2,9,0,0],
            [0,5,0,0,0,3,0,2,8],
            [0,0,9,3,0,0,0,7,4],
            [0,4,0,0,5,0,0,3,6],
            [7,0,3,0,1,8,0,0,0]
        ]
    ];

    const randomPuzzle = puzzles[Math.floor(Math.random() * puzzles.length)];
    setGrid(randomPuzzle);

    const inputs = gridContainer.getElementsByTagName('input');
    for (let i = 0; i < 81; i++) {
        if (inputs[i].value !== '') {
            inputs[i].classList.add('user-input');
        }
    }
}